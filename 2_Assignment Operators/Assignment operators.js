// Assignment operators in JavaScript

// Assignment (=) operator
let a = 5;

// Addition assignment (+=) operator
let b = 10;
b += 3; // Equivalent to b = b + 3;

// Subtraction assignment (-=) operator
let c = 15;
c -= 7; // Equivalent to c = c - 7;

// Multiplication assignment (*=) operator
let d = 20;
d *= 2; // Equivalent to d = d * 2;

// Division assignment (/=) operator
let e = 25;
e /= 5; // Equivalent to e = e / 5;

// Remainder assignment (%=) operator
let f = 30;
f %= 4; // Equivalent to f = f % 4;

// Exponentiation assignment (**=) operator
let g = 2;
g **= 3; // Equivalent to g = g ** 3;

// Concatenation assignment (+=) operator (used with strings)
let message = "Hello, ";
message += "world!"; // Equivalent to message = message + "world!";

console.log("a:", a);
console.log("b:", b);
console.log("c:", c);
console.log("d:", d);
console.log("e:", e);
console.log("f:", f);
console.log("g:", g);
console.log("message:", message);
