// Logical AND (&&) Operator
const andResult1 = true && true;
const andResult2 = true && false;
const andResult3 = false && true;
const andResult4 = false && false;

// Logical OR (||) Operator
const orResult1 = true || true;
const orResult2 = true || false;
const orResult3 = false || true;
const orResult4 = false || false;

// Logical NOT (!) Operator
const notResult1 = !true;
const notResult2 = !false;

console.log("Logical AND Results:");
console.log(andResult1);
console.log(andResult2);
console.log(andResult3);
console.log(andResult4);

console.log("Logical OR Results:");
console.log(orResult1);
console.log(orResult2);
console.log(orResult3);
console.log(orResult4);

console.log("Logical NOT Results:");
console.log(notResult1);
console.log(notResult2);
